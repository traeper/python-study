"""
따옴표 포함시키기
"""

# 작은 따옴표(') 포함시키기
tom1 = "He's name is Tom"

# 큰 따옴표(") 포함시키기
tom2 = '"My name is Tom." he said.'

# 역슬래쉬(\)로 따옴표 포함시키기
tom3 = 'He\'s name is Tom'
tom4 = "\"My name is Tom.\" he said."

"""
여러 줄인 문자열
"""

multiple_lines = """
How to use
multiline string
"""

# 또는 엔터를 나타내는 \n을 사용해도 됩니다.
a = "How to use\nmultiline string"
